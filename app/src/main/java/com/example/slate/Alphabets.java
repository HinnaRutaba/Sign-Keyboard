package com.example.slate;

public class Alphabets {
    private  int index;
    private int sign;

    public Alphabets(){

    }

    public Alphabets( int index, int sign) {
        this.index = index;
        this.sign = sign;
    }

    public int getIndex() {
        return index;
    }

    public int getSign() {
        return sign;
    }
}
